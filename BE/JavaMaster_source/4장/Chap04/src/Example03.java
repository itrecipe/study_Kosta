public class Example03 {
   public static void main(String[] args) {
      int num = 20;
      
      if (num == 10) {
          System.out.println("숫자가 10입니다.");
      }
      else if (num == 15) {
          System.out.println("숫자가 15입니다.");
      }
      else if (num == 20) {
          System.out.println("숫자가 20입니다.");
      }
      else {
          System.out.println("숫자가 존재하지 않습니다.");
      }
   }
}